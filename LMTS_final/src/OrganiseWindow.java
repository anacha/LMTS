    private void jOrganiseApplyButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jOrganiseApplyButtonMouseClicked
        // TODO add your handling code here:


        int j,i;
        ExceptionWindow d;
        String IncFrom,IncTo,ExFrom,ExTo,ListValue = null;
        ArrayList<String> QueryResult = new ArrayList<String>();
        IncFrom = jOrganiseIncludeFromFieldTextField.getText();
        IncTo = jOrganiseIncludeToFieldTextField.getText();
        ExFrom = jOrganiseExcludeFromFieldTextField.getText();
        ExTo = jOrganiseExcludeToFieldTextField.getText();
       
        ListValue=(String) jOrganiseCriteriaList.getSelectedValue();

        if(jOrganiseCriteriaList.getSelectedValue()==null)
        {
            /*8a petaksei kai window*/
            d=new ExceptionWindow("No Organise criteria selected");
            d.setVisible(true);
            return;
        }

        String selection = jOrganiseWindowComboBox.getSelectedItem().toString();

        
        if(IncFrom.length()==0 && IncTo.length()==0 && ExFrom.length()==0 && ExTo.length()==0)
        {
             System.out.print("1");
             if(ListValue.equals("Borrower"))
                ListValue = "b_name";
             if(ListValue.equals("Loaner"))
                ListValue = "l_name";
             if(ListValue.equals("Loan Type"))
                ListValue = "l_type";
             if(ListValue.equals("Date"))
                ListValue = "l_date";
            /*8a petaksei kai window*/
             QueryResult = c.doOrganize(ListValue, IncFrom, IncTo,  ExFrom, ExTo, selection);
            
        }
        else
        {
            System.out.print("2");
            QueryResult = c.doOrganize(ListValue, IncFrom, IncTo,  ExFrom, ExTo, selection);
        }

        Clean_Table(jOrganiseWindowTable);

        ListIterator<String> litr = QueryResult.listIterator();

        int RowsAlloc = QueryResult.size()/6 - 2;
         if (QueryResult.size()/6 > 30) {
             Add_Row(RowsAlloc);
         }

        for(j=0; j < QueryResult.size()/6 ; j++)
        {
            for(i=0;i<6;i++)
            {
                String element = litr.next();
                jOrganiseWindowTable.setValueAt((String)element, j,i);
            }
        }

}//GEN-LAST:event_jOrganiseApplyButtonMouseClicked

