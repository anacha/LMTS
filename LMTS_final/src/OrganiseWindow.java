    private void jOrganiseCriteriaListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jOrganiseCriteriaListMouseClicked
        // TODO add your handling code here:
        String organise_choice= (String) jOrganiseCriteriaList.getSelectedValue();
        System.out.print(organise_choice);

        if(organise_choice.equals("Loan Type")){
            jOrganiseIncludeFromFieldTextField.setEditable(false);
            jOrganiseIncludeFromFieldTextField.setText("");
            jOrganiseIncludeToFieldTextField.setEditable(false);
            jOrganiseIncludeToFieldTextField.setText("");
            jOrganiseExcludeFromFieldTextField.setEditable(false);
            jOrganiseExcludeFromFieldTextField.setText("");
            jOrganiseExcludeToFieldTextField.setEditable(false);
            jOrganiseExcludeToFieldTextField.setText("");
        }
        if(organise_choice.equals("Date") ||
                organise_choice.equals("Amount")    ||
                organise_choice.equals("Rate")      ||
                organise_choice.equals("Borrower")  ||
                organise_choice.equals("Loaner"))   {
            jOrganiseIncludeFromFieldTextField.setEditable(true);
            jOrganiseIncludeToFieldTextField.setEditable(true);
            jOrganiseExcludeFromFieldTextField.setEditable(true);
            jOrganiseExcludeToFieldTextField.setEditable(true);

        }

    }//GEN-LAST:event_jOrganiseCriteriaListMouseClicked

